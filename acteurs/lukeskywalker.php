    Luke Skywalker is een fictief personage en de hoofdpersoon van de originele filmtrilogie van de Star Wars-franchise, gemaakt door George Lucas.
    Gespeeld door Mark Hamill, verscheen Luke voor het eerst in Star Wars (1977), [a] en keerde hij terug in The Empire Strikes Back (1980) en Return of the Jedi (1983). Drie decennia later keerde Hamill terug als Luke in de Star Wars-vervolgtrilogie en verscheen in alle drie de films: The Force Awakens (2015), The Last Jedi (2017) en The Rise of Skywalker (2019). Hij hernam de rol in de aflevering "Chapter 16: The Rescue" (2020) van The Mandalorian, waarin hij het personage vertolkte dat werd gespeeld door een body double met digitale veroudering.
