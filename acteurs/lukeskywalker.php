    Het personage verschijnt ook kort in de prequel-film Episode III - Revenge of the Sith als baby. In het gedecanoniseerde Star Wars Expanded Universe (omgedoopt tot Legends), is Luke een hoofdpersoon in veel verhalen die zich afspelen na Return of the Jedi, waarin hij wordt afgebeeld als een krachtige Jedi Master, de echtgenoot van Mara Jade, vader van Ben Skywalker, en oom van moederszijde van Jaina, Jacen en Anakin Solo.
